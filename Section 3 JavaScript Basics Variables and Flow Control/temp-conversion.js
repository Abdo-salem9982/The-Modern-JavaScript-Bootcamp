let fahrenheit = 50;

// Calculate celsius and store in the celsius varable
// Print that value

let celsius = (fahrenheit - 32) * (5 / 9);
console.log(celsius + " C");

// Calculate kelvin and store in the kelvin varable
// Print that value

let kelvin = (fahrenheit + 459.67) * (5 / 9);
console.log(kelvin + " K");
